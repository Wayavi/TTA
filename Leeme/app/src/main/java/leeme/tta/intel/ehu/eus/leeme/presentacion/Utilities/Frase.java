package leeme.tta.intel.ehu.eus.leeme.presentacion.Utilities;

public class Frase
{
    private String castellano;
    private String euskera;
    private String video;
    private String bideo;

    public String getCastellano() {
        return castellano;
    }

    public void setCastellano(String castellano) {
        this.castellano = castellano;
    }

    public String getEuskera() {
        return euskera;
    }

    public void setEuskera(String euskera) {
        this.euskera = euskera;
    }

    public String getBideo() {
        return bideo;
    }

    public void setBideo(String bideo) {
        this.bideo = bideo;
    }

    public String getVideo() {
        return video;
    }

    public void setVideo(String video) {
        this.video = video;
    }

}
